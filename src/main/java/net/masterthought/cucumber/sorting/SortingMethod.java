package net.masterthought.cucumber.sorting;

/**
 * Supported sorting methods.
 *
 * @author Damian Szczepanik (damianszczepanik@github)
 */
public enum SortingMethod {

    /**
     * Same order as in JSON file.
     */
    NATURAL,
    /**
     * Order by name of the element.
     */
    ALPHABETICAL
}